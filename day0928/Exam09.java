import java.util.Scanner;
class Exam09 
{
	public static void main(String[] args) 
	{
		Scanner sc= new Scanner(System.in);
		int data;
		System.out.println("10000보다 작은 정수를 입력하세요");
		data= sc.nextInt();	
		System.out.println("천의 자리:"+data/1000);
		System.out.println("백의 자리:"+data/100%10);
		System.out.println("십의 자리:"+data/10%10);
		System.out.println("일의 자리:"+data%10);

	}
}
